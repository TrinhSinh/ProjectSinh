package App;

public class Toan {
    public static final String ten="Toan";
    public float diem;
	public Toan() {
		
	}
	public Toan(float diem) {
		super();
		this.diem = diem;
	}
	public float getDiem() {
		return diem;
	}
	public void setDiem(float diem) {
		this.diem = diem;
	}
	public static String getTen() {
		return ten;
	}
    
}
